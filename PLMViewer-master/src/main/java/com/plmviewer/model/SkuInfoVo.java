package com.plmviewer.model;

public class SkuInfoVo {	
	
	private String skuGroupName;
	private String skuName;
	private String skuFullName;
	private String bomSkuStatus;
	private String introDate;
	private String fsIntroDate;
	private String skuTeam;
	
	public SkuInfoVo() {
	}
	
	public String getSkuGroupName() {
		return skuGroupName;
	}
	public void setSkuGroupName(String skuGroupName) {
		this.skuGroupName = skuGroupName;
	}
	public String getSkuName() {
		return skuName;
	}
	public void setSkuName(String skuName) {
		this.skuName = skuName;
	}
	public String getSkuFullName() {
		return skuFullName;
	}
	public void setSkuFullName(String skuFullName) {
		this.skuFullName = skuFullName;
	}
	public String getBomSkuStatus() {
		return bomSkuStatus;
	}
	public void setBomSkuStatus(String bomSkuStatus) {
		this.bomSkuStatus = bomSkuStatus;
	}
	public String getIntroDate() {
		return introDate;
	}
	public void setIntroDate(String introDate) {
		this.introDate = introDate;
	}
	public String getFsIntroDate() {
		return fsIntroDate;
	}
	public void setFsIntroDate(String fsIntroDate) {
		this.fsIntroDate = fsIntroDate;
	}
	public String getSkuTeam() {
		return skuTeam;
	}
	public void setSkuTeam(String skuTeam) {
		this.skuTeam = skuTeam;
	}
	
	
}
